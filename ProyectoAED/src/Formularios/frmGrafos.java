    private void btnBuscarCaminoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCaminoActionPerformed
        // TODO add your handling code here:
           int origen = 0, destino = 0;
        String nombreOrigen, nombreDestino;
        nombreOrigen = (String) cbxEleccionOrigen.getSelectedItem();
        nombreDestino = (String) cbxEleccionDestino.getSelectedItem();
        
          if ("Central".equals(nombreOrigen)) {
            origen = 0;
        }
        if ("Arequipa".equals(nombreOrigen)) {
            origen = 1;
        }
        if ("Pacifico".equals(nombreOrigen)) {
            origen = 2;
        }
        if ("Petithuars".equals(nombreOrigen)) {
            origen = 3;
        }
          if (origen == destino) {
            Font fuente = new Font("Arial", Font.BOLD, 18);
            txtKMAcumulados.setFont(fuente);
            JOptionPane.showMessageDialog(this, "Estas en:" + nombreOrigen);
            txtKMAcumulados.setText("Intenta de nuevo!!");
            txtKMAcumulados.setEnabled(false);
            txtKMAcumulados.setForeground(Color.RED);

        } else {
            AlgoritmoDijkstra Dijkstra = new AlgoritmoDijkstra(arboles, Numerotope, origen, destino);
            Dijkstra.dijkstra();

            Font fuente = new Font("Arial", Font.BOLD, 18);
            txtKMAcumulados.setFont(fuente);
            txtKMAcumulados.setText("No hay ningun error :)");
            txtKMAcumulados.setForeground(Color.BLUE);
            txtKMAcumulados.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Estas en:" + nombreDestino);
            txtKilometrosRecorridos.setText(Dijkstra.getAcumulado() + " KM");
        }
        
        
    }//GEN-LAST:event_btnBuscarCaminoActionPerformed

