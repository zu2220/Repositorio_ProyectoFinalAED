    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        limpiarTabla();
        Nodo [] tabla= th1.getTabla();
        
        for(int i= 0; i < size; i++){
            if(tabla[i] == null){
                o[0]= i;
                o[1]= null;
                o[2]= null;
                o[3]= null;
                o[4]= null;
                o[5]= null;
            } else{
                o[0]= i;
                o[1]= tabla[i].getEstudiante().getCodigo();
                o[2]= tabla[i].getEstudiante().getNombre();
                o[3]= tabla[i].getEstudiante().getApellido();
                o[4]= tabla[i].getEstudiante().getDNI();
                o[5]= tabla[i].getEstudiante().getCodigo();
            
            }
            dtm.addRow(o);
            
        
        }
        
    }//GEN-LAST:event_btnListarActionPerformed

